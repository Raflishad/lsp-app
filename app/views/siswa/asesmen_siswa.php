        <script>
            function tampilkanHalaman(nomor) {
                const tabelPertama = document.getElementById('tabel-pertama');
                const tabelKedua = document.getElementById('tabel-kedua');

                if (nomor === 1) {
                    tabelPertama.classList.remove('hidden');
                    tabelKedua.classList.add('hidden');
                } else if (nomor === 2) {
                    tabelPertama.classList.add('hidden');
                    tabelKedua.classList.remove('hidden');
                }

                // Reset semua tombol
                document.querySelectorAll('button[data-halaman]').forEach(btn => {
                    btn.classList.remove('bg-blue-600', 'text-white');
                    btn.classList.add('border', 'border-gray-300', 'hover:bg-gray-100');
                });

                // Aktifkan tombol yang dipilih
                document.querySelectorAll(`button[data-halaman="${nomor}"]`).forEach(btn => {
                    btn.classList.add('bg-blue-600', 'text-white');
                    btn.classList.remove('border', 'border-gray-300', 'hover:bg-gray-100');
                });
            }
        </script>